#!/usr/bin/env node
// Pre-bake every package's tools.manifest.json into the landing's
// /api/discovery route, so the route is Edge-Runtime-safe (no fs at runtime).
//
// Wired into the landing's build via package.json scripts.prebuild —
// any time pnpm regen-manifests runs, this regenerates the baked file too.

import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(HERE, "..");

const PKGS_WITH_TOOLS = [
  "identity",
  "identity-attest",
  "mercadopago",
  "whatsapp",
  "banking",
  "facturacion",
  "shipping",
];

const manifests = PKGS_WITH_TOOLS.map((p) =>
  JSON.parse(readFileSync(resolve(ROOT, "packages", p, "tools.manifest.json"), "utf-8")),
);

const out = `// Generated by scripts/gen-discovery-manifests.mjs from packages/<name>/tools.manifest.json.
// Edit those files (via \`pnpm regen-manifests\`), not this one.
/* eslint-disable */
export const MANIFESTS = ${JSON.stringify(manifests, null, 2)} as const;
`;

writeFileSync(
  resolve(ROOT, "apps/landing/src/app/api/discovery/manifests.generated.ts"),
  out,
);

const total = manifests.reduce((acc, m) => acc + m.tools.length, 0);
console.log(`ok: ${manifests.length} manifests baked, ${total} tools indexed.`);
