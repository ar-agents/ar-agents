#!/usr/bin/env node
// Pre-bake every package's tools.manifest.json into the landing's
// /api/discovery route so the route is Edge-Runtime-safe (no fs at runtime).
//
// Wired into the landing's build via package.json scripts.prebuild.

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));            // apps/landing/scripts
const APP_ROOT = resolve(HERE, "..");                            // apps/landing
const MONO_ROOT = resolve(APP_ROOT, "..", "..");                 // ar-agents
const PKGS_WITH_TOOLS = [
  "identity",
  "identity-attest",
  "constancia",
  "mi-argentina",
  "firma-digital",
  "gde-tad",
  "mercadopago",
  "mercadolibre",
  "whatsapp",
  "banking",
  "facturacion",
  "shipping",
  "igj",
  "boletin-oficial",
  "ap2",
  "agentic-commerce-bridge",
  "mcp",
];

// On Vercel monorepo deploys the landing root is uploaded with the workspace
// packages symlinked into node_modules; tools.manifest.json is reachable via
// node_modules/@ar-agents/<name>/tools.manifest.json. Locally and in CI,
// reach the source files directly.
function findManifest(pkg) {
  const candidates = [
    resolve(MONO_ROOT, "packages", pkg, "tools.manifest.json"),
    resolve(APP_ROOT, "node_modules", "@ar-agents", pkg, "tools.manifest.json"),
  ];
  for (const c of candidates) {
    if (existsSync(c)) return c;
  }
  throw new Error(
    `Cannot find tools.manifest.json for @ar-agents/${pkg}. Looked in:\n  ${candidates.join("\n  ")}`,
  );
}

const manifests = PKGS_WITH_TOOLS.map((p) => JSON.parse(readFileSync(findManifest(p), "utf-8")));

const out = `// Generated by apps/landing/scripts/gen-discovery-manifests.mjs from
// packages/<name>/tools.manifest.json. Edit those files (via \`pnpm regen-manifests\`),
// not this one.
/* eslint-disable */
export const MANIFESTS = ${JSON.stringify(manifests, null, 2)} as const;
`;

writeFileSync(
  resolve(APP_ROOT, "src/app/api/discovery/manifests.generated.ts"),
  out,
);

const total = manifests.reduce((acc, m) => acc + m.tools.length, 0);
console.log(`ok: ${manifests.length} manifests baked, ${total} tools indexed.`);
